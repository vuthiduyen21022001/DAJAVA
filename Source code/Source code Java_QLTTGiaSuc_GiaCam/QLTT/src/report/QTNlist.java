
package report;


public class QTNlist {
    private String maqt;
    private String mavatnuoi;
    private String tenvn;
    private String mata;
    private String mathuoc;
    private String manv;
    private String thoigian;
    private String tinhtrangsk;
    private String mota; 

    public QTNlist(String maqt, String mavatnuoi, String tenvn, String mata, String mathuoc, String manv, String thoigian, String tinhtrangsk, String mota) {
        this.maqt = maqt;
        this.mavatnuoi = mavatnuoi;
        this.tenvn = tenvn;
        this.mata = mata;
        this.mathuoc = mathuoc;
        this.manv = manv;
        this.thoigian = thoigian;
        this.tinhtrangsk = tinhtrangsk;
        this.mota = mota;
    }

    /**
     * @return the maqt
     */
    public String getMaqt() {
        return maqt;
    }

    /**
     * @param maqt the maqt to set
     */
    public void setMaqt(String maqt) {
        this.maqt = maqt;
    }

    /**
     * @return the mavatnuoi
     */
    public String getMavatnuoi() {
        return mavatnuoi;
    }

    /**
     * @param mavatnuoi the mavatnuoi to set
     */
    public void setMavatnuoi(String mavatnuoi) {
        this.mavatnuoi = mavatnuoi;
    }

    /**
     * @return the tenvn
     */
    public String getTenvn() {
        return tenvn;
    }

    /**
     * @param tenvn the tenvn to set
     */
    public void setTenvn(String tenvn) {
        this.tenvn = tenvn;
    }

    /**
     * @return the mata
     */
    public String getMata() {
        return mata;
    }

    /**
     * @param mata the mata to set
     */
    public void setMata(String mata) {
        this.mata = mata;
    }

    /**
     * @return the mathuoc
     */
    public String getMathuoc() {
        return mathuoc;
    }

    /**
     * @param mathuoc the mathuoc to set
     */
    public void setMathuoc(String mathuoc) {
        this.mathuoc = mathuoc;
    }

    /**
     * @return the manv
     */
    public String getManv() {
        return manv;
    }

    /**
     * @param manv the manv to set
     */
    public void setManv(String manv) {
        this.manv = manv;
    }

    /**
     * @return the thoigian
     */
    public String getThoigian() {
        return thoigian;
    }

    /**
     * @param thoigian the thoigian to set
     */
    public void setThoigian(String thoigian) {
        this.thoigian = thoigian;
    }

    /**
     * @return the tinhtrangsk
     */
    public String getTinhtrangsk() {
        return tinhtrangsk;
    }

    /**
     * @param tinhtrangsk the tinhtrangsk to set
     */
    public void setTinhtrangsk(String tinhtrangsk) {
        this.tinhtrangsk = tinhtrangsk;
    }

    /**
     * @return the mota
     */
    public String getMota() {
        return mota;
    }

    /**
     * @param mota the mota to set
     */
    public void setMota(String mota) {
        this.mota = mota;
    }

}