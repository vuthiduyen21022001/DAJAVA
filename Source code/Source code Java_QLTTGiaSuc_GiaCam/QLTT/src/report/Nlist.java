
package report;


public class Nlist {
   private String mavn;
   private String loaivn;
   private String tenvn;
   private String slnhap;
   private String tlnhap;
   private String gianhap;
   private String ngaynhap;
   private String ghichu; 

    public Nlist(String mavn, String loaivn, String tenvn, String slnhap, String tlnhap, String gianhap, String ngaynhap, String ghichu) {
        this.mavn = mavn;
        this.loaivn = loaivn;
        this.tenvn = tenvn;
        this.slnhap = slnhap;
        this.tlnhap = tlnhap;
        this.gianhap = gianhap;
        this.ngaynhap = ngaynhap;
        this.ghichu = ghichu;
    }

    /**
     * @return the mavn
     */
    public String getMavn() {
        return mavn;
    }

    /**
     * @param mavn the mavn to set
     */
    public void setMavn(String mavn) {
        this.mavn = mavn;
    }

    /**
     * @return the loaivn
     */
    public String getLoaivn() {
        return loaivn;
    }

    /**
     * @param loaivn the loaivn to set
     */
    public void setLoaivn(String loaivn) {
        this.loaivn = loaivn;
    }

    /**
     * @return the tenvn
     */
    public String getTenvn() {
        return tenvn;
    }

    /**
     * @param tenvn the tenvn to set
     */
    public void setTenvn(String tenvn) {
        this.tenvn = tenvn;
    }

    /**
     * @return the slnhap
     */
    public String getSlnhap() {
        return slnhap;
    }

    /**
     * @param slnhap the slnhap to set
     */
    public void setSlnhap(String slnhap) {
        this.slnhap = slnhap;
    }

    /**
     * @return the tlnhap
     */
    public String getTlnhap() {
        return tlnhap;
    }

    /**
     * @param tlnhap the tlnhap to set
     */
    public void setTlnhap(String tlnhap) {
        this.tlnhap = tlnhap;
    }

    /**
     * @return the gianhap
     */
    public String getGianhap() {
        return gianhap;
    }

    /**
     * @param gianhap the gianhap to set
     */
    public void setGianhap(String gianhap) {
        this.gianhap = gianhap;
    }

    /**
     * @return the ngaynhap
     */
    public String getNgaynhap() {
        return ngaynhap;
    }

    /**
     * @param ngaynhap the ngaynhap to set
     */
    public void setNgaynhap(String ngaynhap) {
        this.ngaynhap = ngaynhap;
    }

    /**
     * @return the ghichu
     */
    public String getGhichu() {
        return ghichu;
    }

    /**
     * @param ghichu the ghichu to set
     */
    public void setGhichu(String ghichu) {
        this.ghichu = ghichu;
    }
}
