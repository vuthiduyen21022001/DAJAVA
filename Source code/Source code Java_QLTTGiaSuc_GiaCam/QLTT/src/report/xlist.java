
package report;


public class xlist {
   private String maban;
   private String mavatnuoi;
   private String slx;
   private String tlx;
   private String giax;
   private String ngayx;    

    public xlist(String maban, String mavatnuoi, String slx, String tlx, String giax, String ngayx) {
        this.maban = maban;
        this.mavatnuoi = mavatnuoi;
        this.slx = slx;
        this.tlx = tlx;
        this.giax = giax;
        this.ngayx = ngayx;
    }

    /**
     * @return the maban
     */
    public String getMaban() {
        return maban;
    }

    /**
     * @param maban the maban to set
     */
    public void setMaban(String maban) {
        this.maban = maban;
    }

    /**
     * @return the mavatnuoi
     */
    public String getMavatnuoi() {
        return mavatnuoi;
    }

    /**
     * @param mavatnuoi the mavatnuoi to set
     */
    public void setMavatnuoi(String mavatnuoi) {
        this.mavatnuoi = mavatnuoi;
    }

    /**
     * @return the slx
     */
    public String getSlx() {
        return slx;
    }

    /**
     * @param slx the slx to set
     */
    public void setSlx(String slx) {
        this.slx = slx;
    }

    /**
     * @return the tlx
     */
    public String getTlx() {
        return tlx;
    }

    /**
     * @param tlx the tlx to set
     */
    public void setTlx(String tlx) {
        this.tlx = tlx;
    }

    /**
     * @return the giax
     */
    public String getGiax() {
        return giax;
    }

    /**
     * @param giax the giax to set
     */
    public void setGiax(String giax) {
        this.giax = giax;
    }

    /**
     * @return the ngayx
     */
    public String getNgayx() {
        return ngayx;
    }

    /**
     * @param ngayx the ngayx to set
     */
    public void setNgayx(String ngayx) {
        this.ngayx = ngayx;
    }
}
